a = int(input("Enter the first value (number):"))
b = int(input("Enter the second value (number):"))
c = int(input("Enter the third value (number):"))

print("Before swapping, the values are:")

print("a =", a)
print("b =", b)
print("c =", c)

a, b, c = b, c, a

print("After swapping, the values are:")

print("a =", a)
print("a =", b)
print("c =", c)